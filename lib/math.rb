def addition(num1, num2)
  add = num1 + num2
  return add
end

def subtraction(num1, num2)
  subtr = num1 - num2
  return subtr
end

def division(num1, num2)
  divi = num1 / num2
  return divi
end

def multiplication(num1, num2)
  mult = num1 * num2
  return mult
end

def modulo(num1, num2)
  mod = num1 % num2
  return mod
end

def square_root(num)
  sq = Math.sqrt(num)
  return sq
end